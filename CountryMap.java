	public static final int mapWidth = countryMap[0].length;
	//Horizontal size, number of columns.
	
	public static final int mapHeight = countryMap.length;
	//Vertical size, number of rows.
	
	public static final int countrySize = calculateCountrySize();
	//Number of areas into which the country is divided.
	
	private static final int calculateCountrySize() {
		int x = 0;
		for (int i = 0; i < mapHeight; i++) {
			for (int j = 0; j < mapWidth; j++) {
				if (countryMap[i][j] != 0) x++;
			}
		};
		return x;
	}
	
